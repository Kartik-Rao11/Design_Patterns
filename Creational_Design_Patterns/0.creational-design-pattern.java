/*
 * Creational Design Pattern : These patterns foucses on object creation.They
 * aim to enhance flexibility and efficiency in object creation, allowing
 * systems to remain independent of how their objects are constructed, composed,
 * and represented. This approach helps streamline the instantiation process and
 * can lead to more maintainable and adaptable code.
 * 
 * 1. Factory Method Design Pattern
 * This pattern is typically helpful when it's necessary to separate the
 * construction of an object from its implementation.
 * With the use of this design pattern, objects can be produced without having
 * to define the exact class of object to be created.
 * 
 * 2. Abstract Factory Method Design Pattern
 * Abstract Factory pattern is almost similar to Factory Pattern and is
 * considered as another layer of abstraction over factory pattern.
 * Abstract Factory patterns work around a super-factory which creates other
 * factories.
 * 
 * 3. Singleton Method Design Pattern
 * Of all, the Singleton Design pattern is the most straightforward to
 * understand.
 * It guarantees that a class has just one instance and offers a way to access
 * it globally.
 * 
 * 4. Prototype Method Design Pattern
 * Prototype allows us to hide the complexity of making new instances from the
 * client.
 * The concept is to copy an existing object rather than creating a new instance
 * from scratch, something that may include costly operations.
 * 
 * 
 * 5. Builder Method Design Pattern
 * To “Separate the construction of a complex object from its representation so
 * that the same construction process can create different representations.”
 * Builder pattern is used
 * It helps in constructing a complex object step by step and the final step
 * will return the object.
 */