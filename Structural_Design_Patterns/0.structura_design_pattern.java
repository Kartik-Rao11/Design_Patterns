/*
 * Structural design patterns help define how objects and classes are structured
 * to form large, flexible systems. They focus on class composition and object
 * relationships to simplify design.
 * 
 * List of Structural Design Patterns
 * Pattern Purpose
 * Adapter : Converts one interface into another expected by the client.
 * Bridge : Decouples abstraction from implementation.
 * Composite : Composes objects into tree structures to represent hierarchies.
 * Decorator : Dynamically adds new behaviors to an object.
 * Facade : Provides a simplified interface to a complex system.
 * Flyweight : Minimizes memory usage by sharing common data.
 * Proxy : Controls access to another object.
 */